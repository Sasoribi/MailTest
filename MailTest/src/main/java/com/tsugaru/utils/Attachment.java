package com.tsugaru.utils;

public class Attachment {
	
	private Object attachement;
	private String attachementName;
	private String attachementLocations;
	public Object getAttachement() {
		return attachement;
	}
	public void setAttachement(Object attachement) {
		this.attachement = attachement;
	}
	public String getAttachementName() {
		return attachementName;
	}
	public void setAttachementName(String attachementName) {
		this.attachementName = attachementName;
	}
	public String getAttachementLocations() {
		return attachementLocations;
	}
	public void setAttachementLocations(String attachementLocations) {
		this.attachementLocations = attachementLocations;
	}
	
}
